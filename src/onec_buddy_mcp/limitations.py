def append_tool_limitations(tool_name: str, response: str, *, enabled: bool = True) -> str:
    """Add a stable limitation section without rewriting error responses."""

    if not enabled or response.lstrip().startswith("Ошибка:"):
        return response

    return (
        f"## Ответ\n\n{response}\n\n"
        f"## Ограничения инструмента\n\n{TOOL_LIMITATIONS[tool_name]}\n\n"
        "## Требуемая проверка\n\n"
        "Проверьте существенные утверждения, BSL-код и применимость рекомендаций "
        "в документации нужной версии платформы и в контексте проекта."
    )
